public class Student {
    private String idNum;
    private double gpa;

    // getIdNum
    public String getIdNum() {
        return idNum;
    }

    // setIdNum
    public void setIdNum(String idNum) {
        this.idNum = idNum;
    }

    // getGPA
    public double getGPA() {
        return gpa;
    }

    // setGPA
    public void setGPA(double gpa) {
        this.gpa = gpa;
    }
}
