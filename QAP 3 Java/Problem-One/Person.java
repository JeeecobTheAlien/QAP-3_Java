
// Person.java

public class Person {
    private String name;
    private int age;
    private String gender;

    // getName
    public String getName() {
        return name;
    }

    // setName
    public void setName(String name) {
        this.name = name;
    }

    // getAge
    public int getAge() {
        return age;
    }

    // setAge
    public void setAge(int age) {
        this.age = age;
    }

    // getGender
    public String getGender() {
        return gender;
    }

    // setGender
    public void setGender(String gender) {
        this.gender = gender;
    }
}